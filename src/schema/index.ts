import { z } from "zod";

export const OrderSchema = z.object({
  name: z.string().min(1, "Tu nombre es obligatorio"),
  total: z.number().min(1, "Hay errores en tu pedido"),
  order: z.array(
    z.object({
      id: z.number(),
      name: z.string(),
      price: z.number(),
      quantity: z.number().min(1),
      subtotal: z.number(),
    }),
  ),
});

export const SearchSchema = z.object({
  search: z
    .string()
    .trim()
    .min(1, "El término de búsqueda no puede estar vacío"),
});

export const ProductSchema = z.object({
  name: z
    .string()
    .trim()
    .min(1, { message: "El Nombre del Producto no puede ir vacio" }),
  price: z
    .string()
    .trim()
    .transform((value) => parseFloat(value))
    .refine((value) => value > 0, { message: "Precio no valido" })
    .or(z.number().min(1, { message: "La Categoria es Obligatoria" })),
  categoryId: z
    .string()
    .trim()
    .transform((value) => parseInt(value))
    .refine((value) => value > 0, { message: "La Categoria es Obligatoria" })
    .or(z.number().min(1, { message: "La Categoria es Obligatoria" })),
  imageUrl: z.string().min(1, { message: "La Imagen es Obligatoria" }),
});
